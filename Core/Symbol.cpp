//----------------------------------------------------------------------------
Symbol::Symbol()
:   _type(Invalid) {}
//----------------------------------------------------------------------------
Symbol::Symbol(TypeId type, const StringSlice& cstr)
:   _type(type), _cstr(cstr) {}
//----------------------------------------------------------------------------
Symbol::~Symbol() {}
//----------------------------------------------------------------------------
Symbol::Symbol(const Symbol& other)
: _type(other._type), _cstr(other._cstr) {}
//----------------------------------------------------------------------------
Symbol& Symbol::operator =(const Symbol& other) {
    _type = other._type;
    _cstr = other._cstr;
    return *this;
}

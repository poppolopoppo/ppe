FString ToString(const RTTI::FAtom& atom) {
    FOStringStream oss;
    atom.Format(oss);
    return oss.str();
}
//----------------------------------------------------------------------------
FWString ToWString(const RTTI::FAtom& atom) {
    FWOStringStream oss;
    atom.Format(oss);
    return oss.str();
}

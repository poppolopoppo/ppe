FString ToString(const RTTI::FAtom& atom) {
    FStringBuilder oss;
    atom.Format(oss);
    return oss.ToString();
}
//----------------------------------------------------------------------------
FWString ToWString(const RTTI::FAtom& atom) {
    FWStringBuilder oss;
    atom.Format(oss);
    return oss.ToString();
}

//----------------------------------------------------------------------------
float FGenericPlatformMaths::FP16_to_FP32(u16 u) {
    union {
        u32 intval;
        float floatval;
    } t1;
    u32 t2, t3;
    t1.intval = u & 0x7fff; // Non-sign bits
    t2 = u & 0x8000; // Sign bit
    t3 = u & 0x7c00; // Exponent
    t1.intval <<= 13; // Align mantissa on MSB
    t2 <<= 16; // Shift sign bit into position
    t1.intval += 0x38000000; // Adjust bias
    t1.intval = (t3 == 0 ? 0 : t1.intval); // Denormals-as-zero
    t1.intval |= t2; // Re-insert sign bit
    return t1.floatval;
}

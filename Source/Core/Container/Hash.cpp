FTextWriter& operator <<(FTextWriter& oss, const hash_t& h) {
    return oss << '#' << (const void*)h._value;
}
//----------------------------------------------------------------------------
FWTextWriter& operator <<(FWTextWriter& oss, const hash_t& h) {
    return oss << L'#' << (const void*)h._value;
}

//----------------------------------------------------------------------------
// Gets a unique ID for T
//----------------------------------------------------------------------------
template <typename T>
struct TComponent {
    static constexpr ComponentTag Tag() {
        return ComponentTag( Meta::TTypeHash<T>::value() );
    }
};
